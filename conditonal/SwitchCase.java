package conditonal;

public class SwitchCase {

	public static void main(String[] args) {
		int daysofweek=6;
		switch(daysofweek) {
		case 1:
			System.out.print("The day is 1");
			break;
		case 2:
			System.out.print("The day is 2");
			break;
		case 3:
			System.out.print("The day is 3");
			break;
		case 4:
			System.out.print("The day is 4");
			break;
		default:
			System.out.print("The day is something else");
			break;
		}

	}

}
